    private void loadDataFromDatabase() {
    try {
        tableModel.setRowCount(0); // Hapus semua data di tabel
        String query = "SELECT * FROM table_matakuliah";
        PreparedStatement ps = connection.prepareStatement(query);
        ResultSet rs = ps.executeQuery(query);

        if (rs != null) {
            while (rs.next()) {
                int id = rs.getInt("id_matakuliah");
                String kode = rs.getString("kode_matakuliah");
                String nama_matakuliah = rs.getString("nama_matakuliah");
                String jumlahsks = rs.getString("jumlah_sks");
                String semester = rs.getString("semester");
                String dosen = rs.getString("dosen");
            

                Object[] row = {id, kode, nama_matakuliah, jumlahsks, semester, dosen};
                tableModel.addRow(row);
            }
            rs.close();
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
}
    
  
