    private void jButtonTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahActionPerformed
        // TODO add your handling code here:

       try {
        // Ambil data dari form
        String kode = jTextFieldKodeMataKuliah.getText().trim();
        String namaMatakuliah = jTextFieldNamaMataKuliah.getText().trim();
        String jumlahsks = (String) jComboBoxJumlahSKS.getSelectedItem(); // Ambil langsung dari combobox
        String semester = (String) jComboBoxSemester.getSelectedItem();
        String dosen = jTextFieldDosen.getText().trim();

        // Validasi input
        if (kode.isEmpty() || namaMatakuliah.isEmpty() || jumlahsks.isEmpty() || semester.isEmpty() || dosen.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field wajib diisi!", "Validasi Gagal", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Query untuk memasukkan data ke dalam tabel
        String query = "INSERT INTO table_matakuliah (kode_matakuliah, nama_matakuliah, jumlah_sks, semester, dosen) VALUES (?, ?, ?, ?, ?)";

        // Menggunakan PreparedStatement untuk menghindari SQL Injection
        try (PreparedStatement ps = connection.prepareStatement(query)) {
            ps.setString(1, kode);
            ps.setString(2, namaMatakuliah);
            ps.setString(3, jumlahsks); // Tidak perlu parsing angka karena data langsung dari combobox
            ps.setString(4, semester);
            ps.setString(5, dosen);

            // Eksekusi query
            ps.executeUpdate();

            // Tampilkan pesan sukses
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan!", "Sukses", JOptionPane.INFORMATION_MESSAGE);

            // Muat ulang data dari database dan bersihkan form
            loadDataFromDatabase();
            clearForm();
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal menambahkan data: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButtonTambahActionPerformed

