    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        // TODO add your handling code here:
        int selectedRow = jTableFormMatakuliah.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih data yang ingin diedit!", "Edit Data", JOptionPane.WARNING_MESSAGE);
            return;
        }

       
        String kode = jTextFieldKodeMataKuliah.getText();
        String nama = jTextFieldNamaMataKuliah.getText();
        String jumlahSKS = (String) jComboBoxJumlahSKS.getSelectedItem();
        String semester = (String) jComboBoxSemester.getSelectedItem();
        String dosen = jTextFieldDosen.getText();

        tableModel.setValueAt(kode, selectedRow, 0);
        tableModel.setValueAt(nama, selectedRow, 1);
        tableModel.setValueAt(jumlahSKS, selectedRow, 2);
        tableModel.setValueAt(semester, selectedRow, 3);
        tableModel.setValueAt(dosen, selectedRow, 4);

        clearForm();
        JOptionPane.showMessageDialog(this, "Data berhasil diperbarui!", "Sukses", JOptionPane.INFORMATION_MESSAGE);
    
    }//GEN-LAST:event_jButtonEditActionPerformed

